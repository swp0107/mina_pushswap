#include "libft.h"

void	ft_binary(int nbr)
{
	int div;

	div = 128;
	while (div >= 1)
	{
		if (nbr >= div)
		{
			write(1, "1", 1);
		}
		if (nbr < div)
		{
			write(1, "0", 1);
		}
		nbr = nbr % div;
		div = div / 2;
	}
}
