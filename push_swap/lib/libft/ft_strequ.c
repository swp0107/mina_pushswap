#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	if (s1 != NULL && s2 != NULL)
	{
		while (s1[a] || s2[a])
		{
			if (s1[a] == s2[a])
				b++;
			a++;
		}
		if (a == b)
			return (1);
	}
	return (0);
}
